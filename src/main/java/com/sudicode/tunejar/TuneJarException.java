package com.sudicode.tunejar;

/**
 * Exception thrown by TuneJar.
 */
public class TuneJarException extends Exception {

    /**
     * Construct a new <code>TuneJarException</code> with the specified cause.
     *
     * @param cause The cause
     */
    public TuneJarException(final Throwable cause) {
        super(cause);
    }

}
